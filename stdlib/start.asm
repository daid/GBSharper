std_start:
    call __init
    call tmp_init
    call _function_main

    di
    halt
    db $DD ; ill instruction, will hard-halt on hardware, crash/abort various emulators

tmp_init:
.WaitVBlank:
	ld a, [$FF44]
	cp 144
	jp c, .WaitVBlank
    ld a, 0
	ld [$FF40], a

    ; Copy the tile data
    ld de, Tiles
    ld hl, $9000
    ld bc, TilesEnd - Tiles
    call Memcopy

    ; Copy the tilemap
    ld de, Tilemap
    ld hl, $9800
    ld bc, TilemapEnd - Tilemap
    call Memcopy

    ; Copy the paddle tile
    ld de, Paddle
    ld hl, $8000
    ld bc, PaddleEnd - Paddle
    call Memcopy

    ; Copy the ball tile
    ld de, Ball
    ld hl, $8010
    ld bc, BallEnd - Ball
    call Memcopy

    xor a, a
    ld b, 160
    ld hl, $FE00
.ClearOam:
    ld [hl+], a
    dec b
    jp nz, .ClearOam

    ld a, $80
	ld [$FF40], a
    ret

; Copy bytes from one area to another.
; @param de: Source
; @param hl: Destination
; @param bc: Length
Memcopy:
    ld a, [de]
    ld [hl+], a
    inc de
    dec bc
    ld a, b
    or a, c
    jp nz, Memcopy
    ret

